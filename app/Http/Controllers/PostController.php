     */
    public function store(Request $request)
    {
        $data = request()->validate([
            'title' => 'required',
            'text' => 'required',
            'image' => 'required|image',
        ]);

        $imagePath = request('image')->store('uploads','public');

        \App\Models\Post::create([
            'title' => $data['title'],
            'text' => $data['text'],
            'image' => $imagePath,
        ]);

        return redirect('/news/index');
    }

